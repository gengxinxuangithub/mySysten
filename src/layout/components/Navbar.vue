<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />
    <breadcrumb class="breadcrumb-container" />
    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="hover">
        <div class="avatar-wrapper">朱强 <i class="el-icon-arrow-down el-icon--right" /></div>
        <el-dropdown-menu slot="dropdown">
          <el-dropdown-item @click.native="changePasswordVisible = true">
            <span style="display: block">修改密码</span>
          </el-dropdown-item>
          <el-dropdown-item @click.native="logout">
            <span style="display: block">退出登录</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>

    <change-password :password-visible.sync="changePasswordVisible" />
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import ChangePassword from '@/components/ChangePassword'
import { removeToken } from '@/utils/auth.js'
import * as login from '@/api/login.js'

export default {
  components: {
    Breadcrumb,
    Hamburger,
    ChangePassword
  },
  data() {
    return {
      changePasswordVisible: false
    }
  },
  computed: {
    ...mapGetters(['sidebar', 'avatar'])
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    logout() {
      login
        .logOut()
        .then(res => {
          // 统一维护一份退出登陆代码
          if (res.code === 200) {
            removeToken()
            this.$router.push({ path: '/login' })
          } else {
            this.$message(res.message)
          }
        })
        .catch(err => {
          removeToken()
          this.$router.push({ path: '/login' })
        })
    }
  }
}
</script>

